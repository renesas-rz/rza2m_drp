/******************************************************************************
* Function Name: LcdWriteString
* Description  :
* Arguments    : pcode

* Return Value : -
******************************************************************************/
void LcdClear(char *layer, int lines)
{    
	int line_width;

#if (LCD_FORMAT == LCD_FORMAT_YCBCR422)
	line_width = LCD_WIDTH*2;
#endif
#if (LCD_FORMAT == LCD_FORMAT_RGB565)
	line_width = LCD_WIDTH*2;
#endif
#if (LCD_FORMAT == LCD_FORMAT_CLUT4)
	line_width = LCD_WIDTH/2;
#endif

	if (lines == -1)
		lines = LCD_HEIGHT;	/* entire screen */

	memset(layer,0,line_width * lines);
}
/******************************************************************************
* Function Name: LcdWriteString
* Description  : Display character string in overlap buffer
* Arguments    : pcode
*              :   A pointer to the character string to be displayed
*              : x
*              :   X coordinate to display character string
*              : y
*              :   Y coordinate to display character string
*              : color
*              :   character string color
* Return Value : -
******************************************************************************/
void LcdWriteString(uint8_t *pcode, uint32_t x, uint32_t y, RGB_enum_t color, char *layer)
{
    int i;
    int len;
     
    len = strlen(pcode);

	/* Write string to LCD */
    for (i = 0; i < len; i++)
    {
        LcdWriteChar(*pcode++, (x + (i * FONTDATA_WIDTH)), y, color, layer);
    }

    return;
}

/******************************************************************************
* Function Name: LcdWriteChar
* Description  : Display character in overlap buffer
* Arguments    : code
*              :   Code to be displayed
*              : x
*              :   X coordinate to display character
*              : y
*              :   Y coordinate to display character
*              : color
*              :   character color
* Return Value : -
******************************************************************************/
void LcdWriteChar(uint8_t code, uint32_t x, uint32_t y, RGB_enum_t color, char *layer)
{
    int32_t height;
    int32_t width;
    uint8_t font;

    int32_t width_offset;
    int32_t check_bit;
    for (height = 0; height < FONTDATA_HEIGHT; height++)
    {
        check_bit = 7;
        width_offset = 0;
        font = font_array[code-0x20][height][width_offset++];
        for (width = 0; width < FONTDATA_WIDTH; width++)
        {
            if ((font & (0x1 << (check_bit--))) != 0)
            {
                DrawPoint(width + x, height + y, color, layer);
            }
            if (check_bit < 0)
            {
                check_bit = 7;
                font = font_array[code-0x20][height][width_offset++];
            }
        }
    }

    return;
}

/**************************************************************************//**
 * @brief       DrawPoint
 * @param[in]   x                 : position x
 * @param[in]   y                 : position y
 * @param[in]   color             : 4bit color
 * @retval      None
******************************************************************************/
void DrawPoint(int32_t x, int32_t y , RGB_enum_t color ,char *layer)
{
#if (LCD_FORMAT == LCD_FORMAT_YCBCR422)
	#error "Not implemented"
#endif
#if (LCD_FORMAT == LCD_FORMAT_RGB565)
	unsigned int location = (y * LCD_WIDTH * 2) + (x * 2);
	const struct RGB_colour_t *RGB_value = RGB_format_by_name(color);
	uint16_t r, g, b;

	r = RGB_value->red >> 3;
	g = RGB_value->green >> 2;
	b = RGB_value->blue >> 3;
	*((uint16_t *)(layer + location)) = r << 11 | g << 5 | b;
#endif
#if (LCD_FORMAT == LCD_FORMAT_CLUT4)
	unsigned int location = (y * LCD_WIDTH / 2) + (x / 2);
	int upper = x & 1;

	if (upper)
	{
		*((uint8_t *)(layer + location)) &= 0xF0;
		*((uint8_t *)(layer + location)) |= color;
	}
	else
	{
		*((uint8_t *)(layer + location)) &= 0x0F;
		*((uint8_t *)(layer + location)) |= (color << 4);
	}
#endif

}

