/**************************************************************************//**
 * @brief       DrawLine
 * @param[in]   x                 : position x
 * @param[in]   y                 : position y
 * @param[in]   color             : 4bit CLUT No // No use until now
 * @retval      None
******************************************************************************/
void DrawLine(int32_t x1, int32_t y1, int32_t x2, int32_t y2, RGB_enum_t color, char *layer)
{
	int32_t dx=x2-x1;
    int32_t dy=y2-y1;
    int32_t sx=1;
    int32_t sy=1;
    int32_t i;
    int32_t de;


    if( dx<0 )
    {
        dx *= -1;
        sx *= -1;
    }

    if( dy<0 )
    {
        dy *= -1;    sy *= -1;
    }

    DrawPoint(x1, y1, color, layer);

    if( dx>dy )
    {
        for( i=dx, de=i/2; i; i-- )
        {
            x1 += sx;
            de += dy;
            if( de>dx )
            {
                de -= dx;
                y1 += sy;
            }
            DrawPoint(x1, y1, color, layer);
        }
    }
    else
    {
        for( i=dy, de=i/2; i; i-- )
        {
            y1 += sy;
            de += dx;
            if( de>dy )
            {
                de -= dy;
                x1 += sx;
            }
            DrawPoint(x1, y1, color, layer);
        }
    }
}
