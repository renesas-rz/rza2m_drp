const struct RGB_colour_t *RGB_format_by_name(RGB_enum_t colour)
{
	unsigned char i;

	for (i = 0; i < ARRAY_SIZE(RGB_colour); i++) 
	{
		if (RGB_colour[i].RGB_colour == colour)
			return &RGB_colour[i];
	}

	return NULL;
}

